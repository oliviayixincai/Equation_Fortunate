module chamber;

Chamber *makeSmall (int n) {
    SmallChamber *sc = new SmallChamber{};
    if (n == 1) {
        sc->content = {"|-------------|",
                       "|.............|",
                       "|.............|",
                       "|.......|-----|",
                       "|.......|      ",
                       "|-------|      "};
        return sc;
    } else if (n == 2) {
        sc->content = {"|-------------|",
                       "|.............|",
                       "|....|---|....|",
                       "|....|---|....|",
                       "|.............|",
                       "|-------------|"};
        return sc;
    } else if (n == 3) {
        sc->content = {"|----|         ",
                       "|....|----|    ",
                       "|.........|---|",
                       "|---|.........|",
                       "    |----|....|",
                       "         |----|"};
        return sc;
    } else if (n == 4) {
        sc->content = {"|-------------|",
                       "|.............|",
                       "|.............|",
                       "|.............|",
                       "|.............|",
                       "|-------------|"};
        return sc;
    } else if (n == 5) {
        sc->content = {"      |-------|",
                       "      |.......|",
                       "|-----|.......|",
                       "|.............|",
                       "|.............|",
                       "|-------------|"};
    }
}